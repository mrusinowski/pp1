import random
kostka1 = random.randrange(1,6)
kostka2 = random.randrange(1,6)
kostka3 = random.randrange(1,6)
suma = kostka1 + kostka2 + kostka3
print(f"Za pierwszym rzutem kością wyrzucono {kostka1}.")
print(f"Za drugim rzutem kością wyrzucono {kostka2}.")
print(f"Za trzecim rzutem kością wyrzucono {kostka3}.")
print(f"Suma wyrzuconych oczek z wszystkich trzech rzutów wynosi {suma}.")